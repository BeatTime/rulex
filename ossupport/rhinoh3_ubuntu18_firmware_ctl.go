package ossupport

import (
	"os"
	"os/exec"
)

/*
*
* 重启 RULEX
*
 */
func ReStartRulex() error {
	cmd := exec.Command("systemctl", "restart", "rulex")
	cmd.Stdout = os.Stdout
	cmd.Stderr = os.Stderr
	if err := cmd.Run(); err != nil {
		return err
	}
	return nil
}

/*
*
* 直接重启Linux
*
 */
func Reboot() error {
	cmd := exec.Command("reboot")
	cmd.Stdout = os.Stdout
	cmd.Stderr = os.Stderr
	if err := cmd.Run(); err != nil {
		return err
	}
	return nil
}
